#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - it is a function to add two num.
 * @a: it is the frist input.
 * @b: it is the seconde input.
 * Return: a+b.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - it is a function to sub two num.
 * @a: it is the frist input.
 * @b: it is the seconde input.
 * Return: a-b.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - it is a function to mul two num.
 * @a: it is the frist input.
 * @b: it is the seconde input.
 * Return: a*b.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - it is a function to div two num.
 * @a: it is the frist input.
 * @b: it is the seconde input.
 * Return: a/b.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - it is a function to mod two num.
 * @a: it is the frist input.
 * @b: it is the seconde input.
 * Return: a%b.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
